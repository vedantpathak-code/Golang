package main
import "fmt"
func main(){
	var u8 uint8 = 224
	var u16 uint16 = 65433
	fmt.Println("unsigned 8-bit integer:", u8)
	fmt.Println("unsigned 16-bit integer:", u16)

	var i8 int8 = -128
	var i16 int16 = 3276
	fmt.Println("sinned 8-bit integer:", i8)
	fmt.Println("sinned 16-bit integer:", i16)
}
